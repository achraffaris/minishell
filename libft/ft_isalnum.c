#include "libft.h"

int	ft_isalnum(int ascii)
{
	if ((ascii >= '0' && ascii <= '9') || (ascii >= 'A' && ascii <= 'Z')
		|| (ascii >= 'a' && ascii <= 'z'))
	{
		return (1);
	}
	else
		return (0);
}
